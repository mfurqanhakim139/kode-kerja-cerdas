def sapa_nama(nama): # 'nama' adalah parameter
    print(f"Halo, {nama}! Apa kabar?")

def tambah(angka1, angka2): # 'angka1' dan 'angka2' adalah parameter
    hasil = angka1 + angka2
    print(f"Hasil penjumlahan {angka1} + {angka2} = {hasil}")

# Memanggil fungsi dengan argumen
sapa_nama("Andi") # "Andi" adalah argumen untuk parameter 'nama'
sapa_nama("Citra") # "Citra" adalah argumen
tambah(5, 3) # 5 dan 3 adalah argumen
tambah(100, -50)
